import 'dart:async';

import 'package:flutter/material.dart';

class AnalysisViewModel extends ChangeNotifier {
  final List<String> tips = [
    '?????: ????? ?????? ?? ???????.',
    '?????: ???? ????? ?????????.',
    '?????: ????? ????? ????????? ??????.',
  ];

  int tipIndex = 0;
  Timer? _timer;

  void start() {
    _timer?.cancel();
    _timer = Timer.periodic(const Duration(seconds: 3), (_) {
      tipIndex = (tipIndex + 1) % tips.length;
      notifyListeners();
    });
  }

  @override
  void dispose() {
    _timer?.cancel();
    super.dispose();
  }
}
